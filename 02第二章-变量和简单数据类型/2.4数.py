# 加
print(2 + 6)
# 减
print(2 - 6)
# 乘
print(2 * 6)
# 除
print(2 / 6)
# 乘方(2的n次方)
print(2 ** 6)

# 设置先后执行顺序
print(2 + 3 / 4)
print((2 + 3) / 4)


# 浮点数（小数）
print(0.2 * 0.3)
print(0.1 + 0.2)
# 任意两个数相除时，结果总是浮点数
print(4/2)
print(10 * 2)

# 数字中的下划线
num = 12_000_000
print(num)

# 同时给多个变量赋值
x, y, z = 1, 2, 3
print(x, y)

# 常量通常用全部大写字母来表示
NUM = 3
print(NUM)

# 练习
print(4 + 4)
print(10 - 2)
print(2 * 4)
print(16 / 2)
# // 双斜杠代表，只取整数部分，只有当有值为浮点数时结果才会显示浮点数
print(16 // 2)
print(16 // 2.5)

